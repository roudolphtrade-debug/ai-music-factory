export const translations: Record<Lang, Dict> = { fr, en, es, zh, pt, ar };

function resolve(dict: Dict, key: string): unknown {
  return key.split(".").reduce<unknown>((acc, part) => {
    if (acc && typeof acc === "object" && part in (acc as Record<string, unknown>)) {
      return (acc as Record<string, unknown>)[part];
    }
    return undefined;
  }, dict);
}

export function translate(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>,
): string {
  let value = resolve(translations[lang], key);
  if (value === undefined) value = resolve(translations.fr, key);
  if (typeof value !== "string") return key;
  if (vars) {
    return value.replace(/\{(\w+)\}/g, (_, k) =>
      vars[k] !== undefined ? String(vars[k]) : `{${k}}`,
    );
  }
  return value;
}

export function translateArray(lang: Lang, key: string): string[] {
  const value = resolve(translations[lang], key) ?? resolve(translations.fr, key);
  return Array.isArray(value) ? (value as string[]) : [];
}
